    private void selec2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selec2ActionPerformed
        // Metodo del radio boton seleccionar.
        // NO permite tratar dos (o mas) opciones seleccionadas. 
        // En dicho caso, siempre trata la primer seleccion de la lista.
        // Se soluciona con Button Group
        if (arg.isSelected()) {
            destino.setText("Destino: Argentina.");
        } else if (peru.isSelected()) {
            destino.setText("Destino: Peru.");
        } else if (chile.isSelected()) {
            destino.setText("Destino: Chile.");
        } else {
            destino.setText("Seleccione una opcion.");
        }
    }//GEN-LAST:event_selec2ActionPerformed

