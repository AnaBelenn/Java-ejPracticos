    private void mostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarActionPerformed
        // Metodo Text Field:
        String nombre= nomb.getText();
        if (nombre.isEmpty()) {
            txtnomb.setText("Ingrese un nombre.");
        } else {
            txtnomb.setText("Su nombre es: "+nombre);
        }
        String ed= edad.getText();
        if (ed.isEmpty()) {
            txtedad.setText("Ingrese un numero.");
        } else {
            txtedad.setText("Su edad es: "+ed+" años.");
        }
    }//GEN-LAST:event_mostrarActionPerformed
    
