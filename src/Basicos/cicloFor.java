package Basicos;

public class cicloFor {

    public static void main(String[] args) {
        System.out.println("Valor de i con iteracion FOR:");
        for (int i = 1; i <= 10; i++) {
            System.out.println("i= " + i);
        }

        System.out.println("DECREMENTO de i con iteracion FOR: ");
        for (int a = 10; a >= 1; a--) {
            System.out.println("i= " + a);
        }

    }

}
